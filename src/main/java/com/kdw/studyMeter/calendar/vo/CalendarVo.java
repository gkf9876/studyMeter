package com.kdw.studyMeter.calendar.vo;

public class CalendarVo {
	public String date;
	public String contents;
	public String schYearMonthDate;
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	public String getSchYearMonthDate() {
		return schYearMonthDate;
	}
	public void setSchYearMonthDate(String schYearMonthDate) {
		this.schYearMonthDate = schYearMonthDate;
	}
}
